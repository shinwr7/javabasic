package math1;

public class Math1 {

	public static void main(String[] args) {
		// Math는 자바에서 이미 기본적으로 제공하는 기능이라
		// import 없이 사용가능함.
		int a = -1024;
		System.out.println(Math.abs(a));//absolute number 절댓값
		
		double b = 3.14;
		System.out.println(Math.ceil(b));
		
		double c = 19.9 ;
		System.out.println(Math.floor(c));
		
		
		
	}

}
